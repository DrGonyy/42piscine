int	ft_sqrt(int nb)
{
	int	x;

	if (nb == 1)
		return (1);
	x = 1;
	while (nb / x >= x)
	{
		if ((nb == x * x) && (nb % x == 0))
			return (x);
		x++;
	}
	return (0);
}
