#include "bsq.h"

int	ft_atoi(char *s, int *i)
{
	int	value;

	if (*s > '9' || *s < '0')
		return (-1);
	value = 0;
	while (s[*i] <= '9' && s[*i] >= '0')
		value = value * 10 + (s[(*i)++] - '0');
	return (value);
}
